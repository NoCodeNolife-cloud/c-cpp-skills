#include <bits/stdc++.h>

using namespace std;

int main() {

    int test_integer = 100;
    cout << test_integer << endl;

    /**
     * default
     */
    cout << "dec is " << dec << test_integer << endl;

    /**
     * oct
     */
    cout << "oct is " << oct << test_integer << endl;

    /**
     * hex
     */
    cout << "hex is " << hex << test_integer << endl;

    return 0;
}
